    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        try {
           Class.forName("org.gjt.mm.mysql.Driver");

            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/Inventario","root","");
            JOptionPane.showMessageDialog(null, "Conectado");
            String cod,nom,cos,uni,pro,not;
            String sql="";
            cod = codigo.getText();
            nom = nombre.getText();
            cos = costo.getText();
            uni = unidades.getText();
            pro = prov.getText();
            not = nota.getText();
             
            sql = "INSERT INTO Productos (codigo,nombre,costo,unidades,proveedor,notas) VALUES(?,?,?,?,?,?)";
            PreparedStatement psd = con.prepareStatement(sql);
            psd.setString(1, cod);
            psd.setString(2, nom);
            psd.setString(3, cos);
            psd.setString(4, uni);
            psd.setString(5, pro);
            psd.setString(6, not);
            
            int n=psd.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Registro guardado con exito");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error es"+e);
        }
    }//GEN-LAST:event_agregarActionPerformed

